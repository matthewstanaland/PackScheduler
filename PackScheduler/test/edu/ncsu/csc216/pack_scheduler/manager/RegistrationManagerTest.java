	/**
     * Tests the login() method in RegistrationManger. Tests successful login cases for a student and the 
     * registrar while also considering failed login cases for mismatched ids and passwords.
     */
	@Test
	public void testLogin() {
		 // IMPORTANT: I am assuming that manager.logout() sets the current user back to null instead of
        // the registrar like it was written to. That is a bug that I believe needs to be fixed.
        
        // Put students in the directory so that they can login
        manager.getStudentDirectory().loadStudentsFromFile(STUDENT_FILE);
        
        // successful student login
        assertTrue(manager.login("efrost", "pw"));
        assertEquals("efrost", manager.getCurrentUser().getId());
        manager.logout(); // logout the current user
        
        // student with wrong password
        assertFalse(manager.login("efrost", "wp"));
        
        // student not in directory
        Exception e1 = assertThrows(IllegalArgumentException.class, () -> manager.login("ferost", "pw"));
        assertEquals("User doesn't exist.", e1.getMessage());
        
        // set up registrar credentials and test registrar login success and fails
        
        Properties prop = new Properties();
        try (InputStream input = new FileInputStream("registrar.properties")) {
            prop.load(input);
            

            // correct registrar credentials
            assertTrue(manager.login(prop.getProperty("id"), prop.getProperty("pw")));
            assertEquals(prop.getProperty("id"), manager.getCurrentUser().getId());
            manager.logout(); // logout the current user
            
            // incorrect registrar id
            Exception e2 = assertThrows(IllegalArgumentException.class, () -> manager.login("meforeal", prop.getProperty("pw")));
            assertEquals("User doesn't exist.", e2.getMessage());
            
            // incorrect registrar pw
            assertFalse(manager.login(prop.getProperty("id"), "nope"));
        } catch (IOException e) {
            fail();
        }
	}

	/**
     * Tests the logout() method in RegistrationManger. Login of a Student is verified and then the Student 
     * is logged out and the value of the currentUser is checked. This process is repeated for the Registrar.
     */
	@Test
	public void testLogout() {
	    // Add students to the directory and log one of them in
	    manager.getStudentDirectory().loadStudentsFromFile(STUDENT_FILE);
	    assertTrue(manager.login("efrost", "pw"));
	    
	    // now, logout the student and check that the currentUser is null
	    manager.logout();
	    assertNull(manager.getCurrentUser());
	    
	    // Now, login the registrar and check that logging out results in null again
	    Properties prop = new Properties();
        try (InputStream input = new FileInputStream("registrar.properties")) {
            prop.load(input);
            assertTrue(manager.login(prop.getProperty("id"), prop.getProperty("pw")));
            manager.logout();
            
            assertNull(manager.getCurrentUser());
        } catch (IOException e) {
            fail();
        }
	}

